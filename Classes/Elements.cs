namespace tic_tac_toe
{
    public class Elements
    {
        private int x;
        private int y;

        public Elements(int x, int y)
        {
            this.x = x;
            this.y = y;
        }

        public int getx()
        {
            return x;
        }

        public int gety()
        {
            return y;
        }
    }
}
